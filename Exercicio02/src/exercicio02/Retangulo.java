public class Retangulo {
    private int x1;
    private int x2;
    private int y1;
    private int y2;
    
    
    public Retangulo() {
        x1 = 0;
        x2 = 0;
        y1 = 0;
        y2 = 0;
    }
    
    public void setX1(){
        this.x1=x1;   
    }
    
    
    public void setX2(){
        this.x2=x2;   
    }
    
    public void setY1(){
        this.y1=y1;   
    }
    
    
    public void setY2(){
        this.y2=y2;   
    }
    
} 
    


